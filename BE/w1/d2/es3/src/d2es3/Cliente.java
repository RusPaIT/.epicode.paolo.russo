package d2es3;

public class Cliente {
	
	String codCliente;
	String nomeCognome;
	String email;
	String dataIscrizione;
	
	public Cliente(
			String codCliente, 
			String nomeCognome, 
			String email, 
			String dataIscrizione) {
		this.codCliente = codCliente;
		this.nomeCognome = nomeCognome;
		this.email = email;
		this.dataIscrizione = dataIscrizione;
		
	}

}
